// Na początku będziemy tworzyć bardzo proste aplikacje konsolowe
// Żeby coś wypluć w konsoli używamy:
console.log('Tekst');
// Zobaczysz tekst na wyjściu i dodatkowo dodawany jest znak nowej linii
// Więc jeśli drugi raz wypluję jakiś tekst, to znajdzie się w nowej linijce:
console.log('Tekst, ale w nowej linii');
// Oczywiście jak to bywa w programowaniu możemy przypisać coś do zmiennej i wyświetlić:
let liczba = 3;
console.log('Jakaś ilość czegoś: ' + liczba);
// Dodatkowo widzimy tu jak JavaScript radzi sobie z różnymi typami
// Połączyłem liczbę ze stringiem znakiem "+", który jest też znakiem łączenia ciągu znaków (nie tylko z liczbami)
// JS stwierdził, że skoro mamy to wyświetlić, to on sobie to zamieni na string i pokaże na ekranie